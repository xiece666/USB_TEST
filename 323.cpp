const ll N=1510;
int n,ans,rt;
int dp[2][N];
vector<int>g[N];
bitset<N>fl;
void dfs(int u){
    dp[1][u]=1,dp[0][u]=0;
    for(int v:g[u]){
        dfs(v);
        dp[0][u]+=dp[1][v];
        dp[1][u]+=min(dp[0][v],dp[1][v]);
    }
}
signed main(){
    while(~scanf("%d",&n)){
        fl=ans=0;
        for(int i=1;i<=n;i++)g[i].clear();
        for(int i=1,x,y,z;i<=n;i++){
            read(x,y),x++;
            while(y--)read(z),z++,g[x].ebk(z),fl[z]=1;
        }
        for(int i=1;i<=n;i++)if(!fl[i])rt=i;
        dfs(rt);
        wrt(min(dp[0][rt],dp[1][rt]),'\n');
    }
    return 0;
}