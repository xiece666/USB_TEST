template <typename T> inline void read(T &x){
    x=0;T f=1;char c=getchar();
    for(;!isdigit(c);c=getchar()) if(c=='-')f=-1;
    for(;isdigit(c);c=getchar()) x=(x<<1)+(x<<3)+(c^48);
    x*=f;
}
template<typename T,typename ...Args>void read(T &x,Args&...args){read(x),read(args...);}
template <typename T> void wrt(T x){
    if(x<0) x=-x,putchar('-');
    if(x>9) wrt(x/10);
    putchar(x%10+48);
}
template <typename T> void wrt(T x,char c){wrt(x),putchar(c);}
template <typename T1,typename T2> void wrt(T1 x1,char c1,T2 x2,char c2){wrt(x1,c1),wrt(x2,c2);}
int n,D;
bool dfs(int dep,int a,int b){
    if(a<b)swap(a,b);
    if(dep==D)return (a==n);
    if(b&&n%__gcd(a,b))return 0;
    if((a<<(D-dep))<n)return 0;
    dep++;
    if(b&&a<n&&dfs(dep,a*2,b))return 1;
    if(a<n&&dfs(dep,a*2,a))return 1;
    if(a<n&&dfs(dep,a+b,a))return 1;
    if(b&&a<n&&dfs(dep,a+b,b))return 1;
    if(b&&b<n&&dfs(dep,b*2,b))return 1;
    if(b&&b<n&&dfs(dep,b*2,a))return 1;
    if(a==b)return 0;
    if(b&&dfs(dep,a-b,b))return 1;
    if(dfs(dep,a-b,a))return 1;
    return 0;
}
int main(){
    read(n);
    for(D=1;;D++)
        if(dfs(0,1,0))break;
    wrt(D);
    return  0;
}
